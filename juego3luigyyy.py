class Ghost:
    def __init__(self, position, speed):
        self.position = position
        self.speed = speed
        self.target = pacman

    def move(self):
        # Use A* algorithm to find shortest path to target
        open_list = [(0, self.position)]
        came_from = {}
        cost_so_far = {self.position: 0}

        while open_list:
            current_cost, current_pos = heapq.heappop(open_list)

            if current_pos == self.target:
                break

            for next_pos in [(current_pos + vector(0, 5)), (current_pos + vector(0, -5)), 
                             (current_pos + vector(5, 0)), (current_pos + vector(-5, 0))]:
                if tiles[int(next_pos.y) // 20][int(next_pos.x) // 20] == 0:
                    continue

                new_cost = cost_so_far[current_pos] + 1
                if next_pos not in cost_so_far or new_cost < cost_so_far[next_pos]:
                    cost_so_far[next_pos] = new_cost
                    priority = new_cost + abs(self.target - next_pos)
                    heapq.heappush(open_list, (priority, next_pos))
                    came_from[next_pos] = current_pos

        # Choose next position in shortest path as direction
        next_pos = came_from[self.target]
        self.speed = (next_pos - self.position) / 20
        self.position = next_pos

    def collide(self):
        if abs(pacman - self.position) < 20:
            return True
        else:
            return False

